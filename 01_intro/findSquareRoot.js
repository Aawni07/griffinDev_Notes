//- by using built-in Method (Math.sqrt(num)). //=> this takes a number and returns its squareRoot.
//> if '0' or '+num' is passed, returns the square root of the number.
//> anything apart from the WholeNumber, returns NaN

const num1 = 2.23;
const num2 = -4;
const num3 = 'Hello World..!'

const result1 = Math.sqrt(num1)
const result2 = Math.sqrt(num2)
const result3 = Math.sqrt(num3)

console.log(`square root of ${num1} is ${result1}`);
console.log(`square root of ${num2} is ${result2}`);
console.log(`square root of ${num3} is ${result3}`);





